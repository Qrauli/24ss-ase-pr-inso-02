package at.ase.respond.common;

/**
 * Represents the biological sex of a person.
 */
public enum Sex {
    /**
     * Indicates that the biological sex of the person is unknown.
     */
    UNKNOWN,
    /**
     * Indicates that the biological sex of the person is female.
     */
    FEMALE,
    /**
     * Indicates that the biological sex of the person is male.
     */
    MALE
}
